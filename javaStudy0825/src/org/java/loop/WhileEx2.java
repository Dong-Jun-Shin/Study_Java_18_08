package org.java.loop;

public class WhileEx2 {

	public static void main(String[] args) {
		System.out.println("while");
		
		//2단 ~ 9단까지 구구단 출력 2*1=2 2*2=4...
		
		int i=2;
		
		while(i<10) {
			System.out.println(i+"단입니다.");
			
			int j=1;
			
			while(j<10) {
				
				System.out.println(i+"*"+j+"="+(i*j));
				
				j++;
			}
			i++;
		}
	}
}
