package org.java.operator;

public class OperatorEx14 {

	public static void main(String[] args) {
		System.out.println("馆汗巩");

		for (int i = 0; i < 10; i++) {
				//贸府巩, 备泅何, 角青何
			System.out.println(i+" ");
		}
		
		for (int i = 10; i > 0; i--) {
			//贸府巩, 备泅何, 角青何
		System.out.println(i+" ");
		}
	}
}
