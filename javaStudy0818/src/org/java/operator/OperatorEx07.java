package org.java.operator;

public class OperatorEx07 {

	public static void main(String[] args) {
		System.out.println("대입연산자");
		
		int num=10;
		System.out.println(num);
		
		System.out.println();
		System.out.println("복합 대입 연산자");
		System.out.println();
		
		num=num+10;
		System.out.println(num);
		
		num+=10;
		System.out.println(num);
		
		num-=10;
		System.out.println(num);
		
		num*=10;
		System.out.println(num);
		
		num/=10;
		System.out.println(num);
		
		
	}
}
